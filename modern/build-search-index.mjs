#!/usr/bin/env node
// Builds modern/search-index.js — the full-text search index used by the
// modern reader (modern/app.js). Run from the repository root or anywhere:
//
//   node modern/build-search-index.mjs
//
// Re-run whenever manual pages (*.htm) change, then commit the regenerated
// modern/search-index.js alongside them.
'use strict';

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), '..');
const outFile = join(repoRoot, 'modern', 'search-index.js');

// Pages that are navigation shells rather than readable topics.
const EXCLUDE = new Set([
  '_index.htm',
  '_search.htm',
  'contents.htm',
  'contents_legacy.htm',
  'default.htm',
  'index.html',
  'manual.htm'
]);

const NAMED_ENTITIES = {
  nbsp: ' ', amp: '&', lt: '<', gt: '>', quot: '"', apos: "'",
  ndash: '–', mdash: '—', hellip: '…', copy: '©',
  reg: '®', trade: '™', ldquo: '“', rdquo: '”',
  lsquo: '‘', rsquo: '’', bull: '•', middot: '·',
  eacute: 'é', agrave: 'à', uuml: 'ü', ouml: 'ö',
  auml: 'ä', sect: '§', para: '¶', deg: '°',
  times: '×', divide: '÷', frac12: '½', frac14: '¼'
};

function decodeEntities(text) {
  return text
    .replace(/&#x([0-9a-f]+);/gi, (_m, hex) => String.fromCodePoint(parseInt(hex, 16)))
    .replace(/&#(\d+);/g, (_m, dec) => String.fromCodePoint(Number(dec)))
    .replace(/&([a-z]+);/gi, (m, name) => {
      const key = name.toLowerCase();
      return Object.prototype.hasOwnProperty.call(NAMED_ENTITIES, key) ? NAMED_ENTITIES[key] : m;
    });
}

function stripTags(html) {
  return html
    .replace(/<script[\s\S]*?<\/script>/gi, ' ')
    .replace(/<style[\s\S]*?<\/style>/gi, ' ')
    .replace(/<!--[\s\S]*?-->/g, ' ')
    // Keep a space where block boundaries were so words don't run together.
    .replace(/<[^>]+>/g, ' ');
}

// Mirror the reader's content extraction: prefer the legacy help template's
// page body cell, then common fallbacks, then the whole <body>.
function extractBodyHtml(html) {
  const patterns = [
    /<td[^>]*class="[^"]*page_body[^"]*"[^>]*>([\s\S]*?)<\/td>/i,
    /<div[^>]*class="[^"]*page_body[^"]*"[^>]*>([\s\S]*?)<\/div>/i,
    /<td[^>]*id="tdpage"[^>]*>([\s\S]*?)<\/td>/i,
    /<main[^>]*>([\s\S]*?)<\/main>/i,
    /<body[^>]*>([\s\S]*?)<\/body>/i
  ];
  for (const pattern of patterns) {
    const match = html.match(pattern);
    if (match) return match[1];
  }
  return html;
}

function extractTitle(html, path) {
  const heading = html.match(/<[^>]+class="[^"]*heading[^"]*"[^>]*>([\s\S]*?)<\//i) ||
    html.match(/<h1[^>]*>([\s\S]*?)<\/h1>/i);
  if (heading) {
    const text = decodeEntities(stripTags(heading[1])).replace(/\s+/g, ' ').trim();
    if (text) return text;
  }
  const title = html.match(/<title[^>]*>([\s\S]*?)<\/title>/i);
  if (title) {
    const text = decodeEntities(stripTags(title[1])).replace(/\s+/g, ' ').trim();
    if (text && !/^aspdnsf\s*-\s*1000$/i.test(text)) return text;
  }
  return path.replace(/\.html?$/i, '').replace(/[_-]+/g, ' ').replace(/\b\w/g, c => c.toUpperCase());
}

const pages = [];
const files = readdirSync(repoRoot)
  .filter(name => /\.htm$/i.test(name) && !EXCLUDE.has(name.toLowerCase()))
  .sort();

for (const file of files) {
  const raw = readFileSync(join(repoRoot, file), 'utf8');
  const bodyHtml = extractBodyHtml(raw);
  const text = decodeEntities(stripTags(bodyHtml)).replace(/\s+/g, ' ').trim();
  if (!text) continue;
  pages.push({ p: file, t: extractTitle(raw, file), x: text });
}

const payload = {
  generated: new Date().toISOString(),
  pages
};

const banner = '// Generated by modern/build-search-index.mjs — do not edit by hand.\n' +
  '// Rebuild with: node modern/build-search-index.mjs\n';
writeFileSync(outFile, banner + 'window.MANUAL_SEARCH_INDEX = ' + JSON.stringify(payload) + ';\n', 'utf8');

const bytes = readFileSync(outFile).length;
console.log('Indexed ' + pages.length + ' pages -> modern/search-index.js (' + (bytes / 1024 / 1024).toFixed(2) + ' MB)');
